								<h3>¿Que es conexión libre?</h3>
								<p>Conexión libre

                                    Estas computadoras de escritorio se conectan a las computadoras de servicio empleando software que permite la emulación de algún tipo de terminal. En otros de los casos se les transfiere la información haciendo uso de recursos magnéticos o por trascripción.
                                    
                                    
                                    
                                    
                                    
                                    Cómputo a través de redes
                                    
                                    Esta es la era que esta basada en el concepto de redes de computadoras, en la que la información reside en una o varias computadoras, los usuarios de esta información hacen uso de computadoras para laborar y todas ellas se encuentran conectadas entre si. Esto brinda la posibilidad de que todos los usuarios puedan acceder a la información de todas las computadoras y a la vez que los diversos sistemas intercambien información.</p>

								<hr />

                                <h3>PROTOCOLOS DE COMUNICACIÓN DE RED</h3>
                                <P>Tipos de protocolos de red
                                    Los protocolos para la transmisión de datos en internet más importantes son TCP (Protocolo de Control de Transmisión) e IP (Protocolo de Internet). De manera conjunta (TCP/IP) podemos enlazar los dispositivos que acceden a la red, algunos otros protocolos de comunicación asociados a internet son POP, SMTP y HTTP.
                                    
                                    
                                    
                                    Estos los utilizamos prácticamente todos los días, aunque la mayoría de los usuarios no lo sepan ni conozcan su funcionamiento. Estos protocolos permiten la transmisión de datos desde nuestros dispositivos para navegar a través de los sitios, enviar correos electrónicos, escuchar música online, etc.
                                    
                                    
                                    
                                    Existen varios tipos de protocolos de red:
                                    <H5>Protocolos de comunicación de red:</H5> protocolos de comunicación de paquetes básicos como TCP / IP y HTTP.</BR>
                                    <H5>Protocolos de seguridad de red:</H5> implementan la seguridad en las comunicaciones de red entre servidores, incluye HTTPS, SSL y SFTP.</BR>
                                    <H5>Protocolos de gestión de red:</H5> proporcionan mantenimiento y gobierno de red, incluyen SNMP e ICMP.</P></BR>

